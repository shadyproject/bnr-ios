#import "SPItemStore.h"

@implementation SPItemStore

+ (SPItemStore *)sharedStore
{
    static SPItemStore *sharedStore = nil;
    
    if (!sharedStore) {
        sharedStore = [[super allocWithZone:nil] init];
    }
    
    return sharedStore;
}
@end
